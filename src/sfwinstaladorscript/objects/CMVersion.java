package sfwinstaladorscript.objects;

/**
 *
 * @author jopaulo
 */
public class CMVersion {

    private String _CodVersao;
    private String _rm;
    private String _tipo;
    private String _objeto;
    private String _erro;
    private String _data;

    /**
     * @return the _rm
     */
    public String getRm() {
        return _rm;
    }

    /**
     * @param rm the _rm to set
     */
    public void setRm(String rm) {
        this._rm = rm;
    }

    /**
     * @return the _tipo
     */
    public String getTipo() {
        return _tipo;
    }

    /**
     * @param tipo the _tipo to set
     */
    public void setTipo(String tipo) {
        this._tipo = tipo;
    }

    /**
     * @return the _objeto
     */
    public String getObjeto() {
        return _objeto;
    }

    /**
     * @param objeto the _objeto to set
     */
    public void setObjeto(String objeto) {
        this._objeto = objeto;
    }

    /**
     * @return the _erro
     */
    public String getErro() {
        return _erro;
    }

    /**
     * @param erro the _erro to set
     */
    public void setErro(String erro) {
        this._erro = erro;
    }

    /**
     * @return the _data
     */
    public String getData() {
        return _data;
    }

    /**
     * @param data the _data to set
     */
    public void setData(String data) {
        this._data = data;
    }

    /**
     * @return the _CodVersao
     */
    public String getCodVersao() {
        return _CodVersao;
    }

    /**
     * @param CodVersao the _CodVersao to set
     */
    public void setCodVersao(String CodVersao) {
        this._CodVersao = CodVersao;
    }
}
